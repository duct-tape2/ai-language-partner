// Non-warning lookup: returns null when a phrase has no curated segments. Used
// by the word bank to decide between furigana word segments and the naive
// splitter without triggering the dev warning below for uncovered phrases.
export function findFurigana(phrase: string): FuriToken[] | null {
  return FURIGANA[phrase] ?? null;
}

export function getFurigana(phrase: string): FuriToken[] {
  if (FURIGANA[phrase]) return FURIGANA[phrase];
  // A phrase with kanji but no entry would render unreadable bare kanji to a
  // beginner. Never let that ship silently.
  if (__DEV__ && /[一-龯]/.test(phrase)) {
    console.warn(`[furigana] no readings for "${phrase}" — kanji will render without furigana. Add it to FURIGANA in i18n.ts.`);
  }
  return [{ b: phrase }];
}

