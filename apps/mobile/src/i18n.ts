const TABLES: Record<UiLocale, UiStrings> = { ko: STRINGS_KO, 'zh-Hant': STRINGS_ZH_HANT };

// Mutable table swapped in place. Consumers keep `import { STRINGS }` and read
// properties at render time; Object.assign replaces every top-level section
// reference, so the swap is visible under any module interop (no dependence on
// ESM live bindings surviving Metro/Babel's CJS transform). A locale change is
// driven by the persisted settings state, whose update re-renders the app tree.
export const STRINGS: UiStrings = { ...STRINGS_KO };

let currentUiLocale: UiLocale = 'ko';

export function setUiLocale(locale: UiLocale): void {
  currentUiLocale = locale;
  Object.assign(STRINGS, TABLES[locale] ?? STRINGS_KO);
}

export function getUiLocale(): UiLocale {
  return currentUiLocale;
}

