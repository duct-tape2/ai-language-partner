// 청해 (Listening comprehension) bank - JLPT-listening style. Each item is a
// short two-speaker conversation (A/B) heard via device TTS, followed by one
// comprehension question and 4 choices. Distinct from the dictation/듣기 screen:
// here you listen to a *dialogue* then answer a "누가/언제/얼마/어디" style question,
// the way real JLPT 聴解 items work.
//
// Self-contained: no backend, no i18n lookup. Each line supplies its own
// furigana tokens so the transcript renders readings regardless of the global
// FURIGANA table. Hand-authored everyday scenes (약속 / 쇼핑 / 길안내 / 교실 / 식당).

export type ChoukaiToken = { b: string; r?: string };

export type ChoukaiLine = {
  speaker: 'A' | 'B';
  ja: string; // plain text, spoken via app.speak()
  tokens: ChoukaiToken[]; // furigana for the transcript (kanji chunks carry r)
};

export type ChoukaiItem = {
  id: string;
  level: 'N5' | 'N4';
  situationKo: string; // one-line scene setup, Korean
  lines: ChoukaiLine[]; // the dialogue (spoken in order)
  question: string; // the comprehension question, Korean
  choices: string[]; // 4 choices (Korean or Japanese)
  answerIndex: number; // 0-based index into choices
  explanationKo: string; // why that answer is correct, Korean
};

